public class Student {
    public int id;
    public String name;
    public String surname;
    public int indexNumber;
    public int groupNumber;
    public String email;

    public Student(int Id, String Name, String Surname, int IndexNumber, int GroupNumber, String Email) {
        id = Id;
        name = Name;
        surname = Surname;
        indexNumber = IndexNumber;
        groupNumber = GroupNumber;
        email = Email;
    }
}
